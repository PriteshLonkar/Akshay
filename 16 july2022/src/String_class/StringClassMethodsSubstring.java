package String_class;
// String Class Methods/Functions - substring() 
public class StringClassMethodsSubstring
{
public static void main(String[] args)
{
	String s1 = "velocity";
	System.out.println(s1.substring(4)); //city
	System.out.println(s1.substring(1, 5)); //eloc

	
	String s2 = "pineapple";

	System.out.println(s2.substring(4)); //apple
	System.out.println(s2.substring(0,4)); //pine
	
	
	
}

}
