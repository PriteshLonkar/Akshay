package Polymorphism;
// method overloading - same method with diff parameter
public class data01
{

	//addition method with 2 int parameter
public void Add(int a,int b) 
{
	System.out.println("Addition with 2 int="+ (a+b));
}

//addition method with 3 int parameter
public void Add(int a,int b, int c) 
{
	System.out.println("Addition with 2 int="+( a+b+c));
}

}
