package control_Switch;
//switch control statement with int input
public class Method01
{

	
	public static void main(String[] args) 
	{
	int inp=10;
	
	switch(inp)
	{
	case 11: System.out.println("main");
	break;
	
	case 10: System.out.println("pre");
	break;
	
	default : System.out.println("invalid");
	
	
	
	}
	}
}
